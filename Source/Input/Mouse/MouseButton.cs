namespace HaighFramework.Input;

/// <summary>
/// Buttons on a mouse
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// The left mouse button.
    /// </summary>
    Left = 0,
    /// <summary>
    /// The middle mouse button.
    /// </summary>
    Middle,
    /// <summary>
    /// The right mouse button.
    /// </summary>
    Right,
    /// <summary>
    /// Browser Back
    /// </summary>
    Mouse4,
    /// <summary>
    /// Browser Forward
    /// </summary>
    Mouse5,
}
